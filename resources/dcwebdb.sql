1947年，英国匈牙利裔物理学家丹尼斯·盖伯发明了全息投影术，他因此项工作获得了1971年的诺贝尔物理学奖。全系投影技术从发明开始就一直应用于电子显微技术中，在这个领域中被称为电子全息投影技术，但是全息投影技术一直到1960年激光的发明才取得了实质性的进展。
