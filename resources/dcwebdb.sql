本项目主要研究半导体制冷、制热方式以及基于Arduino的智能物联网设计，熟悉单片机开发环境，学习基本的电路设计与组装，单片机的控制与通信，Android的软件开发等。主要难点存在于电路板的设计、制作，代码的编写、调试上，其中，电路板设计和单片机、Android开发是需要认真攻破的难点，需要我们队员在假期积极学习相关知识，与老师、同学多多交流。初期会购买一些零件组装出基本雏形，并开始编写代码，调试各个模块，获取状态参数，完成原型机。中期要自主设计制作电路板，完善代码，设计外壳，改进提升性能。后期配套Android APP将开发完成，有余力的话会设计IOS系统APP。
Arduino是本项目研究的重点，基于Arduino可以做一些很多有趣的小项目。在本项目中，我们使用Arduino作为中控，控制半导体片的工作状态，读取温度传感器数值，并负责与触摸屏和手机的交互。通过此项目，我们将更加熟悉单片机原理，wifi通信，I2C以及SPI通信方式，巩固学习内容。同时，我们还将额外学习Android软件开发，PCB制板，Soildworks/CAD建模、电路设计等等，这些将大大丰富我们的知识，开阔我们的眼界，提升我们的竞争力。
###四、项目研究的特色
传统冰箱使用压缩机与制冷剂进行制冷，体积庞大且笨重。市面已有的半导体冰箱效率低且功能单一。我们使用半导体片进行制冷，体积小巧且重量轻，适合小体积的冰箱。同时，利用半导体制冷片的工作原理，我们将其正负极反接即可实现在制冷与制热之间转换，实现一机两用。与此同时，我们引入时下流行的物联网概念，将该冰箱通过wifi与手机连接，实现手机操控，定时开关等功能。
