<h4 id="h4-7-2-viewer"><a name="7.2模型显示软件VIEWER" class="reference-link"></a><span class="header-link octicon octicon-link"></span>7.2模型显示软件VIEWER</h4><p>由于这套软件的图形显示部分的工作主要集中在模型渲染上，图形界面的生成并不复杂，所以图形界面库我们选择了相对小巧高速的FLTK。图形渲染的操作比较复杂，调用了大量的OpenGL库函数，还有一些自己编写的画布渲染函数。对于鼠标操作的捕捉和用户与模型的交互部分，全部使用了自己设计的“画布+相机”的程序结构，尽可能地流畅显示旋转中的模型。由于我们的三维显示做到了从任意角度都可以观看模型，所以需要同时渲染四块区域，为了做到四块区域都可以联动地显示模型的不同面以达到“观察模型的任何一个细节”，在程序开发时提出了“窗口和画布分离”的思路，为了适配各种各样不同显示比例和分辨率的显示器，我们预留了许多用于指定显示器分辨率的接口。因此模型的显示位置不可以用常数定位，而是需要先用图形界面接口初始化四个模型窗口的位置，再得到由这四个模型窗口的位置添加动态的偏移量得到的最终渲染后的不同角度的模型的显示位置。</p>
